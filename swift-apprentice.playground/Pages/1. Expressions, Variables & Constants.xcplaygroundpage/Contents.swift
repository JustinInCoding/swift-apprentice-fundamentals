//: ## How a Computer Works
//: The CPU stores the numbers it acts upon in small memory units called registers
//: The CPU can read numbers into registers from the computer’s main memory, known as Random Access Memory (RAM).
//: It can also write the number stored in a register back into RAM.

//: Each time the CPU makes an addition, a subtraction, a read from RAM or a write to RAM, it’s executing a single instruction
//: a compiler, which converts the code into those small machine instructions the CPU knows how to execute.

//: ## Representing Numbers
//: each character within a block of text is represented by a number.
//: An image consists of thousands or millions of picture elements called pixels, where each pixel is a solid color. Each of these solid color pixels is usually represented by three numbers: one for the amount of red, one for the amount of green and one for the amount of blue.

//: each digit of a binary number is a bit.
//: Eight bits make up a byte.
//: Four bits is called a nibble
//: Each register, for example, is usually 32 or 64 bits in length, which is why we speak of 32-bit and 64-bit CPUs.
//: hexadecimal or hex is base 16.

